		<h1>CFPB Data Design Project</h1>
			<p>This project involves creating an Entity Relationship Diagram ("ERD")for the "Tell Your Story" section of the Consumer Financial Protection Agency ("CFPB") website.
			The CFPB website allows consumers to tell the agency about their experience with a financial service or product.</p>
		<h2>Persona</h2>
			<p>	The user of this website is a consumer of financial services or products from a bank, credit union or other financial company. This is a large portion of the adult population and the user could have any education level or tech knowledge. Thus, the site must be fairly simple to understand and navigate.
					A typical user is Jane Doe, 32 years old, single mother of two children.  She is a high school graduate and works at a restaurant.  Jane's car needed repairs but she didn't have enough money to afford them.  Jane went to a ABC Loan Co. and borrowed money.
					The interest rate was very high and it took a long time for Jane to pay back the money.  This was a negative experience for Jane.  She repaired her car but had unpleasant experiences with ABC and paid ABC thousands of dollars.
		<h2> Use Case</h2>
			<p>The consumer posts a story by accessing the CFPB webpage, writing the story in the appropriate box and clicking the "Tell Your Story" button at the bottom of the page. <a href="https://help.consumerfinance.gov/app/tellyourstory"  Visit webpage </a>
